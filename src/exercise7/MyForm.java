    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        addUser EU=new addUser();
        int row=jTable1.getSelectedRow();
        int col;
        if(row!=-1){
            EU.userTitle.setText("Edit User");
            EU.addUserBtn.setText("Save");
            String[] data=new String[7];
            for(col=0;col<7;col++){
                data[col]=(String)jTable1.getValueAt(row, col);
            }
            EU.setData(data);
            String selectedImagePath = data[6];
           ImageIcon ii = new ImageIcon(selectedImagePath);
           Image image = ii.getImage().getScaledInstance(imagePlaceholder.getWidth(), imagePlaceholder.getHeight(), Image.SCALE_SMOOTH);
           imagePlaceholder.setIcon(new ImageIcon(image));
            EU.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(null, "There is no data selected!");
        } 
    }//GEN-LAST:event_jButton2ActionPerformed

